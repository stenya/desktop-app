package helpers

import (
	"crypto/rand"
	"math/big"
)

// RndInt returns a uniform random value in [0, max). It panics if max <= 0.
func RndInt(max int) int {
	if rnd, err := rand.Int(rand.Reader, big.NewInt(int64(max))); err == nil {
		return int(rnd.Int64())
	}
	return 0
}
